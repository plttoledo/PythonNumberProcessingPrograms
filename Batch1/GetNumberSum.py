# Prog03: Create a program that ask user to input 2 numbers. Print the sum of the two numbers.
# User input 2 numbers, print the sum of the two numbers.

user_input1 = float(input("Input the first number: "))
user_input2 = float(input("Input the second number: "))

user_sum = user_input1 + user_input2
print(f"{user_input1} + {user_input2} results to {user_sum}.")