# Prog03: Create a program that ask user to input 2 numbers. Print the difference of the two numbers.
# Get user input two numbers, print the difference of the two

user_input1 = float(input("Enter first number: "))
user_input2 = float(input("Enter second number: "))

get_diff =  user_input1 - user_input2
print(f"{user_input1} minus {user_input2} equals to {get_diff}")